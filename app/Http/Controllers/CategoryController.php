    {
        $id = Category::query()->where('slug',$slug)->first()?->id;
        $productIds = ProductCategory::query()->where('category_id', $id)->get()->pluck('product_id');
        return response()->json([
            'products' => Product::query()->whereIn('id',$productIds)->paginate(10),
        ]);
    }

